# 3) შექმენი ცვლადი age = 0 და while loop იმუშავოს სანამ age < 18 და while შიგნით age = input
#  მომხმარებელმა რომ შეიყვანოს თავისი ასაკი და სანამ ასაკი ნაკლები იქნება 18 მაქამდე შეეკითხოს,
#  და თუ 18-ზე მეტია მაშინ გამოიტანოს "თქვენ სრულწლოვანი ხართ".

age = 0 

while age < 18:
    age = int(input('enter age:'))
    if age >= 18:
        print('თქვენ სრულწლოვანი ხართ')