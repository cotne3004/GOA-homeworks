#3)ახსენით კომენტარის სახით რას ნიშნავს if-else-elif და როგორ მუშაობს იგი

#if(თუ) ნიშნავს იმას რომ თუ პირობაა true(ჭეშმარიტი) მაშინ სრულდება მის ქვეშ მიწერილი მოქმედება
#elif (წინააღმდეგ შემთხვევაში) სრულდება მაშნ როცა if-ი არ შესრულდა და გვინდა სხვა პირობა შვამოწმოთ
#else (არცერთი) სრულდება მაშინ როცა არც if იყო ჭეშმარიტი და არც elif პირობა აეჭირდება